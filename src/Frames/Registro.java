    public void validar(){//METODO PARA VALIDAR LOS DATOS DEL USUARIOs
        String correo = txtCorreo.getText();
        String pass = String.valueOf(txtContrasena.getPassword());
        String nom = txtNombre.getText();
        String rol = cbxRol.getSelectedItem().toString();
        if(!"".equals(correo) || !"".equals(pass) || !"".equals(nom)){//VALIDAR EL NOMBRE Y CONTRASEÑA
            lg.setNombre(nom);
            lg.setCorreo(correo);
            lg.setPass(pass);
            lg.setRol(rol);
            login.Registrar(lg);
            Loging iniciar = new Loging();
            iniciar.setVisible(true);
            dispose();
            
            
        }
        
    }

